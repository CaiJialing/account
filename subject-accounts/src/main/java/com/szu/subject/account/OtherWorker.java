package com.szu.subject.account;

public class OtherWorker {
	int worker_id;
	int worker_no;
	String worker_name;
	String worker_pass;
	int worker_role;
	public int getWorker_id() {
		return worker_id;
	}
	public void setWorker_id(int worker_id) {
		this.worker_id = worker_id;
	}
	public int getWorker_no() {
		return worker_no;
	}
	public void setWorker_no(int worker_no) {
		this.worker_no = worker_no;
	}
	public String getWorker_name() {
		return worker_name;
	}
	public void setWorker_name(String worker_name) {
		this.worker_name = worker_name;
	}
	public String getWorker_pass() {
		return worker_pass;
	}
	public void setWorker_pass(String worker_pass) {
		this.worker_pass = worker_pass;
	}
	public int getWorker_role() {
		return worker_role;
	}
	public void setWorker_role(int worker_role) {
		this.worker_role = worker_role;
	}
}
