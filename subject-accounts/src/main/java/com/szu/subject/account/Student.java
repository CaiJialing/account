package com.szu.subject.account;

public class Student {
	int student_id;
	int student_no;
	int student_password;
	String student_name;
	int class_id;
	int major_id;
	String student_team;
	
	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public int getStudent_no() {
		return student_no;
	}
	public void setStudent_no(int student_no) {
		this.student_no = student_no;
	}
	public int getStudent_password() {
		return student_password;
	}
	public void setStudent_password(int student_password) {
		this.student_password = student_password;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public int getClass_id() {
		return class_id;
	}
	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}
	public int getMajor_id() {
		return major_id;
	}
	public void setMajor_id(int major_id) {
		this.major_id = major_id;
	}
	public String getStudent_team() {
		return student_team;
	}
	public void setStudent_team(String student_team) {
		this.student_team = student_team;
	}
}
